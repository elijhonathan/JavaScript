// Operadores de Comparação
// = é atribuição
// == é comparação
// === é comparação de conteúdo e formato

let numero = "1"
//console.log(numero == "1")
resultado = numero == "1"
console.log(resultado)

console.log(numero == 1)
console.log(numero === 1)

// !=
let marca = "Apple"
console.log( marca != "Xiaomi")
console.log(numero !== 1)

// >, <, >= e <=
let comparar = 800
console.log(comparar > 800)
console.log(comparar < 800)
console.log(comparar >= 800)
console.log(comparar <= 801)



